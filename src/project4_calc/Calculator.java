    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(operation != null && !operation.equals("+")){
            operation = "+";
            jLabel1.setText(first+" "+operation);
        }
        if (!jTextField1.getText().equals("")) {
            if (operation != null) {
                jButton5ActionPerformed(null);
            }
            first = jTextField1.getText();
            operation = "+";
            jLabel1.setText(first + " " + operation);
            jTextField1.setText("");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if(!jTextField1.getText().equals("") && operation != null){
            if(result == null){
                second = jTextField1.getText();
            }else{
                first = result;
            }
            Double f = Double.parseDouble(first);
            Double s = Double.parseDouble(second);
            Double res = null;
            switch (operation) {
                case "+":
                    res = f + s;
                    break;
                case "-":
                    res = f - s;
                    break;
            }
            result = res.toString();
            isDouble();
            jTextField1.setText(result);
            jLabel1.setText(first + " " + operation
                    + " " + second + " = " + result);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void isDouble() {
        if (result.indexOf(".0") == result.length() - 2) {
            result = result.substring(0,
                    result.length() - 2);
        }
    }

